package Index;

public class Operator4 {
	public static void main(String[] args) {
		int a =25,b=20;
		// arithmetic operator;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		///relational operator
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a==b);
		System.out.println(a!=b);
	
		
		
		
	}

}
